operandoA = 3
operandoB = 2
suma = operandoA + operandoB
#print('Resultado de la suma:', suma)
print(f'Resultado suma: {suma}') #-< esta es una forma de imprimir los resultados

resta = operandoA - operandoB
print(f'Resultado de la resta: {resta}')

multiplicacion = operandoA * operandoB
print(f'Resultado de la multiplicacion: {multiplicacion}')

division = operandoA / operandoB
print(f'Resultado division: {division}')

division = operandoA // operandoB #<- devuelve un entero con //
print(f'Resultado division (int): {division}')

modulo = operandoA % operandoB
print(f'Resultado residuo division (modulo): {modulo}')

exponente = operandoA ** operandoB
print(f'Resultado exponente: {exponente}')